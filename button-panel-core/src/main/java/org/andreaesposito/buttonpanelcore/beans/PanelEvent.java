package org.andreaesposito.buttonpanelcore.beans;

public class PanelEvent {

    private String button;
    private int mode;

    public String getButton() {
        return button;
    }

    public void setButton(String button) {
        this.button = button;
    }

    public int getMode() {
        return mode;
    }

    public void setMode(int mode) {
        this.mode = mode;
    }
}
